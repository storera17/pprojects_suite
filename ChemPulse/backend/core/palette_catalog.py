def normalize_palette_key(value: str) -> str:
    """Return a valid palette key, falling back to the default palette."""

    cleaned = str(value or "").strip().lower()
    return cleaned if cleaned in PALETTE_CATALOG else DEFAULT_PALETTE_KEY


def palette_vars(key: str) -> dict[str, str]:
    """Return the CSS variable map for a palette."""

    return PALETTE_CATALOG[normalize_palette_key(key)]["vars"]


def palette_chart_theme(key: str) -> dict[str, Any]:
    """Return the chart theme tokens for a palette."""

    return PALETTE_CATALOG[normalize_palette_key(key)]["chart"]
